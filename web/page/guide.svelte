<div>
    <p>
        <b>Expiry</b> is the time the paste will be available for viewing. After
        this time, the paste will be deleted.
    </p>
    <p>
        <b>Burn after</b> is the number of times the paste can be viewed before it
        is deleted.
    </p>
    <p>
        <b>Privacy</b> determines who can see your paste and if it has encryption.
    </p>
    <ul>
        <li>
            <b>Public</b> - Anyone can see the paste and edit it at their will.
        </li>
        <li>
            <b>Unlisted</b> - Only people with the link can see the paste and can
            edit it.
        </li>
        <li>
            <b>Readonly</b> - Only people with the link can see the paste but they
            need the password to edit it.
        </li>
        <li>
            <b>Private</b> - A password is needed to view and edit the paste. The
            paste is encrypted on server.
        </li>
        <li>
            <b>Secret</b> - A password is needed to view and edit the paste. The
            paste is encrypted on client and on server.
        </li>
    </ul>
</div>

